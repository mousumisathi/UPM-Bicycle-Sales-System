<h4 style= "text-align: left">How to Ride a Bike</h4>
<p> Let’s explain some more tips for smooth bike riding:</p>
<p> 1) Use your rear brake to check your speed and your front one only if you really want to stop. If you grab a handful of front brake on a modern bike it might become a catapult.</p>
<p> 2) Remember when I said clippy shoes were worth it? It’s because they allow you to apply power throughout the whole revolution of the pedal, and to use your whole leg and butt to apply said power. They’re awesome.</p>
<p> 3) Keep in mind also that it’s more efficient to spin than to mash. This means that you’ll be able to deliver more power for longer if you ride in a smaller, easier gear at a higher cadence (RPM) than in a bigger, harder gear at a slower cadence.</p>
<p> 4) Your body should be pretty much still when you’re riding. Your knees should be in line between your hip and ankle, not out as if you’re trying to climb a pole</p>

